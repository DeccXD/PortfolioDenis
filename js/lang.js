// Cambiar idioma dinámicamente
function setLanguage(lang) {
  // Guardar en localStorage
  localStorage.setItem("lang", lang);

  // Guardar en URL
  const url = new URL(window.location);
  url.searchParams.set("lang", lang);
  window.history.replaceState({}, "", url);

  document.documentElement.lang = lang;

  const elements = document.querySelectorAll("[data-i18n]");

  // Fade OUT
  elements.forEach(el => el.classList.add("fade-out"));

  setTimeout(() => {
    elements.forEach(el => {
      const key = el.getAttribute("data-i18n");
      const translation = translations[lang][key];

      if (translation) el.textContent = translation;

      el.classList.remove("fade-out");
      el.classList.add("fade-in");
    });

    setTimeout(() => {
      elements.forEach(el => el.classList.remove("fade-in"));
    }, 400);

    updateActiveLang(lang);
  }, 200);
}

// Botones ES / EN
document.getElementById("btn-es")?.addEventListener("click", () => setLanguage("es"));
document.getElementById("btn-en")?.addEventListener("click", () => setLanguage("en"));

// Idioma inicial
window.addEventListener("DOMContentLoaded", () => {
  const urlParams = new URLSearchParams(window.location.search);
  const langFromURL = urlParams.get("lang");

  const savedLang = localStorage.getItem("lang");

  let defaultLang;

  if (langFromURL) {
    defaultLang = langFromURL;
  } else if (savedLang) {
    defaultLang = savedLang;
  } else {
    defaultLang = navigator.language.startsWith("es") ? "es" : "en";
  }

  setLanguage(defaultLang);
});

document.getElementById("btn-es-footer")?.addEventListener("click", () => setLanguage("es"));
document.getElementById("btn-en-footer")?.addEventListener("click", () => setLanguage("en"));

function updateActiveLang(lang) {
  const ids = [`btn-${lang}`, `btn-${lang}-footer`];

  document.querySelectorAll("[id^='btn-']").forEach(btn => {
    btn.classList.remove("bg-blue-500");
  });

  ids.forEach(id => {
    document.getElementById(id)?.classList.add("bg-blue-500");
  });
}

